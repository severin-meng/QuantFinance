def plot_time(product_name, times, pth_constrs, scramblers, num_paths):
    fig, ax =plt.subplots(1, 2, figsize=(5, 4))
    fig.suptitle(f"Computation Time for {product_name}")
    ax[0].set_title("Absolute Computation Time")
    ax[0].set_xlabel("N")
    ax[0].set_ylabel("Time [ms]")
    ax[0].grid(True)

    ax[1].set_title("Time relative to Pseudorandom Incr")
    ax[1].set_xlabel("N")
    ax[1].set_ylabel("Relative Computation Time")
    ax[1].grid(True)

    ref_time = times[-1]  # pseudorandom time

    for (time, pth_constr_name, scrambler) in zip(times, pth_constrs, scramblers):
        ax[0].loglog(num_paths, time, 'o-', label=f"{pth_constr_name}-{scrambler}")
        ax[1].semilogx(num_paths, time/ref_time, 'o-', label=f"{pth_constr_name}-{scrambler}")

    ax[0].legend()
    ax[1].legend()

    fig.tight_layout()
    plt.show()


