def plot_steps(product="DiP", model="LV", steps=[100], lv="equidist", disc="euler", interp="linear", vol="flat", smpls=50):
    base_dir = r'/home/severin/Scrambling_PathConstruction/csvData'
    full_dir = os.sep.join([base_dir, model_path_map[model], product])
    biasfree_file, _ = get_filename(product, model, UNBIASED_STEPS, lv, disc, interp, vol, UNBIASED_SMPLS)
    unbiased_df = pd.read_csv(os.sep.join([full_dir, biasfree_file]))
    df = pd.DataFrame()
    _, title = get_filename(product, model, steps, lv, disc, interp, vol, smpls)
    measures = []
    for i, step in enumerate(steps):
        filename, _ = get_filename(product, model, step, lv, disc, interp, vol, smpls)
        full_path = os.sep.join([full_dir, filename])
        print(f"reading file {full_path}")
        if i == 0:
            df = pd.read_csv(full_path)
            measures = find_relevant_measures(df, model)
            df.rename(columns={"Result": f"Result_{step}"}, inplace=True)
        else:
            new_df = pd.read_csv(full_path)
            new_df.rename(columns={"Result": f"Result_{step}"}, inplace=True)
            df = pd.merge(df, new_df, on=["Config", "Measure", "Type", "Paths"], suffixes=(None, None))
    configs = df["Config"].unique()
    for measure in measures:
        plot_v_paths = True
        plot_v_time = True
        rows = int(plot_v_paths) + int(plot_v_time)
        cols = len(steps)
        width = 2 + 6 * cols
        height = 1 + rows * 5
        fig, ax = plt.subplots(rows, cols, figsize=(width, height))
        fig.suptitle(title)
        for i in range(len(steps)):
            step = steps[i]
            res_col = f"Result_{step}"
            row_idx = 0
            bias = 0
            if plot_v_paths:
                ax[row_idx][i].set_title(f"{measure} ({step} steps)")
                ax[row_idx][i].set_xlabel("Paths")
                ax[row_idx][i].set_ylabel("Relative Error")
                ax[row_idx][i].grid(True)
                for j, config in enumerate(configs):
                    df_msr = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "Variance")]
                    ref_val = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "RefValue")][res_col].values[0]
                    biasfree_result = unbiased_df[(unbiased_df['Measure'] == measure) & (unbiased_df['Config'] == configs[0]) & (unbiased_df['Type'] == 'RefValue')]["Result"].values[0]
                    bias = np.abs((ref_val-biasfree_result)/biasfree_result)
                    relative_error = np.sqrt(df_msr[res_col].values) / np.abs(ref_val)
                    label = config
                    x_dat = np.log(df_msr['Paths'].values)
                    y_dat = np.log(relative_error)
                    poly = np.polynomial.Polynomial.fit(x_dat, y_dat, deg=1, domain=[-1, 1])
                    label += rf", $\alpha={poly.coef[1]:.2f}$"
                    line, = ax[row_idx][i].loglog(df_msr['Paths'].values, relative_error, label=label)
                    ax[row_idx][i].loglog([df_msr['Paths'].values[0], df_msr['Paths'].values[-1]], [bias, bias], color=line.get_color(), linestyle='dotted')
                ax[row_idx][i].legend()
                row_idx += 1
            if plot_v_time:
                ax[row_idx][i].set_title(f"{measure} ({step} steps)")
                ax[row_idx][i].set_xlabel("Time [ms]")
                ax[row_idx][i].set_ylabel("Relative Error")
                ax[row_idx][i].grid(True)
                for j, config in enumerate(configs):
                    df_msr = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "Variance") & (df['Paths'] >= MIN_PATHS_TIME)]
                    ref_val = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "RefValue")][res_col].values[0]
