def convergence_v_paths(df, measures, configs, ax):
    for i in range(len(measures)):
        measure = measures[i]
        ax[i].set_title(f"{measure} Relative Error")
        ax[i].set_xlabel("Paths")
        ax[i].grid(True)
        for j, config in enumerate(configs):
            df_msr = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "Variance")]
            ref_val = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "RefValue")]['Result'].abs().values[0]
            relative_error = np.sqrt(df_msr['Result'].values) / ref_val
            label = config
            x_dat = np.log(df_msr['Paths'].values)
            y_dat = np.log(relative_error)
            poly = np.polynomial.Polynomial.fit(x_dat, y_dat, deg=1, domain=[-1, 1])
            label += rf", $\alpha={poly.coef[1]:.2f}$"
            ax[i].loglog(df_msr['Paths'].values, relative_error, label=label)
        ax[i].legend()


def convergence_v_time(df, measures, configs, ax, min_paths=MIN_PATHS_TIME):
    for i in range(len(measures)):
        measure = measures[i]
        ax[i].set_title(f"{measure} Relative Error")
        ax[i].set_xlabel("Time [ms]")
        ax[i].grid(True)
        for j, config in enumerate(configs):
            df_msr = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "Variance") & (df['Paths'] >= min_paths)]
            ref_val = df[(df['Measure'] == measure) & (df['Config'] == config) & (df['Type'] == "RefValue")]['Result'].abs().values[0]
            relative_error = np.sqrt(df_msr['Result'].values) / ref_val
            if measure == "Value":
                time = "CompTime"
            else:
                time = "AADTime"
            df_time = df[(df['Measure'] == time) & (df['Config'] == config) & (df['Type'] == "Avg") & (df['Paths'] >= min_paths)]
            df_merged = pd.merge(df_msr, df_time, on="Paths", suffixes=("_v", "_t"))
            ax[i].loglog(df_merged["Result_t"].values, np.sqrt(df_merged['Result_v'].values)/ref_val, label=config)
        ax[i].legend()


def plot_convergence_v_paths(df, measures, configs, title=""):
    fig, ax = plt.subplots(1, len(measures), figsize=(15, 4))
    fig.suptitle(title)
    convergence_v_paths(df, measures, configs, ax)
    plt.show()


def plot_convergence_v_time(df, measures, configs, title="", min_paths=MIN_PATHS_TIME):
    fig, ax = plt.subplots(1, len(measures), figsize=(15, 4))
    fig.suptitle(title)
    convergence_v_time(df, measures, configs, ax, min_paths=min_paths)
    plt.show()


